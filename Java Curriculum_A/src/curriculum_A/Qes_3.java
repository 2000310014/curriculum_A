package curriculum_A;

public class Qes_3 {

	public static void main(String[] args) {
		
	//[概要] バイト型に代入処理
	//[詳細]　10を代入する。
		byte byteNum = 1;
		System.out.println(byteNum);
	// 10を代入。	
		byteNum = 10;
		System.out.println(byteNum);	
		
		
	//[概要]　短整数型に代入処理
	//[詳細]　100を代入する。
		short shortNum = 10;
		System.out.println(shortNum);
	// 100を代入。
		shortNum = 100;
		System.out.println(shortNum);
		
		
	//[概要]　整数型に代入処理
	//[詳細]　1000を代入する。
		int intNum = 100;
		System.out.println(intNum);
	// 1000を代入。
		intNum = 1000;
		System.out.println(intNum);
			
		
	//[概要]　長整数型に代入処理
	//[詳細]　10000を代入する。
		long longNum = 1000;
		System.out.println(longNum);
	// 10000を代入。
		longNum = 10000;
		System.out.println(longNum);
		
		
	//[概要]　単精度浮動小数点型に代入処理
	//[詳細]　9.5を代入する。
		float floatNum = 1.5f;
		System.out.println(floatNum);
	// 9.5を代入。
		floatNum = 9.5f;
		System.out.println(floatNum);
		
		
	//[概要]　倍精度浮動小数点型に代入処理
	//[詳細]　10.5を代入する。
		double doubleNum = 1.123;
		System.out.println(doubleNum);
	// 10.5を代入。
		doubleNum = 10.5;
		System.out.println(doubleNum);
		
		
	//[概要]　文字型に代入処理
	//[詳細]　「a」を代入する。
		char charWord = 'A';
		System.out.println(charWord);
	// 「A」を代入。
		charWord = 'a';
		System.out.println(charWord);
		
		
	//[概要]　文字列型に代入処理
	//[詳細]　「ハロー」を代入する。
		String stringWord = "Hello";
		System.out.println(stringWord);
	// 「ハロー」を代入。
		stringWord = "ハロー";
		System.out.println(stringWord);
		
		
	//[概要]　ブーリアン型に代入処理
	//[詳細]　trueを代入する。
		boolean booleanNum = false;
		System.out.println(booleanNum);
	// true を代入。
		booleanNum = true;
		System.out.println(booleanNum);
	}
	
}

package curriculum_A;

public class Qes_2 {

	public static void main(String[] args) {
	
	//[概要] バイト型の初期化処理
	//[詳細]　1を初期値に設定する。
		byte byteNum = 1;
		System.out.println(byteNum);
		
	//[概要]　短整数型の初期化処理
	//[詳細]　10を初期値に設定する。
		short shortNum = 10;
		System.out.println(shortNum);
		
	//[概要]　整数型の初期化処理
	//[詳細]　100を初期値に設定する。
		int intNum = 100;
		System.out.println(intNum);
			
	//[概要]　長整数型の初期化処理
	//[詳細]　1000を初期値に設定する。
		long longNum = 1000;
		System.out.println(longNum);
		
	//[概要]　単精度浮動小数点型の初期化処理
	//[詳細]　1.5を初期値に設定する。
		float floatNum = 1.5f;
		System.out.println(floatNum);
			
	//[概要]　倍精度浮動小数点型の初期化処理
	//[詳細]　1.123を初期値に設定する。
		double doubleNum = 1.123;
		System.out.println(doubleNum);
			
	//[概要]　文字型の初期化処理
	//[詳細]　「A」を初期値に設定する。
		char charWord = 'A';
		System.out.println(charWord);
			
	//[概要]　文字列型の初期化処理
	//[詳細]　「Hello」を初期値に設定する。
		String stringWord = "Hello";
		System.out.println(stringWord);
				
	//[概要]　ブーリアン型の初期化処理
	//[詳細]　trueを初期値に設定する。
		boolean booleanNum = true;
		System.out.println(booleanNum);
	}
	
}
